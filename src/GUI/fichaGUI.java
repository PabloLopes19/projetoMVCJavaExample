    public fichaDTO carregarDTO(){
        fichaDTO fichadto = new fichaDTO();
        if (txtMatricula.getText().equals(""))
            fichadto.setMatricula(0);
        else
            fichadto.setMatricula(Integer.parseInt(txtMatricula.getText()));
        fichadto.setNome(txtNome.getText());
        return fichadto;
    }

    public void carregarTela(fichaDTO ficha){
        txtMatricula.setText(String.valueOf(ficha.getMatricula()));
        txtNome.setText(ficha.getNome());
    }

    public fichaGUI() {
        initComponents();
        
        fichabo = new fichaBO();
        listObj = fichabo.pesquisar();
        if (listObj.size() > 0)
            carregarTela(listObj.get(0));
    }

